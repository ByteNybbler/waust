#[test]
fn test_struct() {
    #[derive(Serialize)]
    struct Test {
        int: u32,
        seq: Vec<&'static str>,
    }

    let test = Test {
        int: 300,
        seq: vec!["a", "boo"],
    };

    let expected = vec![0x2C, 0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 'a' as u8, 0x03, 0x00, 0x00, 0x00, 'b' as u8, 'o' as u8, 'o' as u8];
    let mut vector = vec![];
    to_writer(&mut vector, &test).unwrap();
    println!("{:?}", vector);
    assert_eq!(vector, expected);
}